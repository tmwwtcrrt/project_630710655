  }

  static double CNY2THB(double value){
    return (value * 5.08);
  }

  static double THB2JPY(double value){
    return (value * 3.95);
  }

  static double JPY2THB(double value){
    return (value * 0.25);
  }

  static double THB2HKD(double value){
